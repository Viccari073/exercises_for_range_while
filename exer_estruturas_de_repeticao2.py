"""
Escreva um programa que escreva na tela, de 1 até 100, de 1 em 1, 2 vezes.
A primeira deve usar a estrutura de repetição for e a segunda a estrutura while.
"""

for n in range(1, 101):
    print(n, end=' ')

print()
print()

c = 1
while c <= 100:
    print(c, end= ' ')
    c += 1

